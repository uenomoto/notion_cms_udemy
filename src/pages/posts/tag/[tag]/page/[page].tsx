export const getStaticPaths: GetStaticPaths = async () => {
  const allTags = await getAllTags();
  // ç©ºã®é…åˆ—
  let params = [];

  // éåŒæœŸå‡¦ç†ãŒåŒæ™‚ã«ï¼’ã¤ã‚ã‚‹ã¨ã†ã¾ãå‹•ã‹ãªã„ã‹ã‚‰Promise.allã‚’ä½¿ç”¨ï¼ˆä»Šå›ã¯allTagsã¨getNumberOfPagesByTagã§ä½¿ã£ã¦ã„ã‚‹ï¼‰
  await Promise.all(
    //å…¨ã¦ã®ã‚¿ã‚°ã‚’å–å¾—ã—
    allTags.map((tag: string) => {
      // ã‚¿ã‚°ã«å¿œã˜ã¦é–¢ä¿‚ã®ã‚ã‚‹è¨˜äº‹ã®ãƒšãƒ¼ã‚¸ãƒãƒ¼ã‚·ãƒ§ãƒ³ã®æ•°ã‚’å‡ºã™
      return getNumberOfPagesByTag(tag).then((numberOfPageByTag: number) => {
        //thenã§æˆåŠŸã—ãŸã‚‰ ãã®ã‚¿ã‚°ã®ã‚ã‚‹è¨˜äº‹ãŒã‚ã‚‹æ•°ã¶ã‚“foræ–‡ã§ç¹°ã‚Šè¿”ã—å‡¦ç†
        for (let i = 1; i <= numberOfPageByTag; i++) {
          params.push({ params: { tag: tag, page: i.toString() } });
        }
      });
    })
  );

  // console.log(params);

  return {
    paths: params,
    fallback: "blocking",
  };
};

